export function translateText(text: string, targetLanguage: string): string {
  const lang = translations[targetLanguage as keyof typeof translations] || translations.en
  
  // First try exact matches
  if (lang[text as keyof typeof lang]) {
    return lang[text as keyof typeof lang]
  }
  
  // If no exact match and target language is not English, return the English version
  if (targetLanguage !== 'en' && translations.en[text as keyof typeof translations.en]) {
    return translations.en[text as keyof typeof translations.en]
  }
  
  // If still no match, return the original text
  return text
}

export function getVoiceSettings(language: string): SpeechSynthesisUtterance {
  const utterance = new SpeechSynthesisUtterance()
  
  // Set language codes for proper voice selection
  const languageCodes: Record<string, string> = {
    'en': 'en-US',
    'sw': 'sw-TZ',
    'ki': 'ki-KE', // Kikuyu - fallback to Kenyan English
    'lu': 'lu-KE', // Luo - fallback to Kenyan English  
    'ka': 'ka-KE', // Kalenjin - fallback to Kenyan English
    'lg': 'lg-UG', // Luganda - fallback to Ugandan English
    'rn': 'rn-UG'  // Runyankole - fallback to Ugandan English
  }
  
  utterance.lang = languageCodes[language] || 'en-US'
  utterance.rate = 0.9 // Slightly slower for better clarity
  utterance.pitch = 1
  utterance.volume = 0.8 // Slightly lower volume
  
  // Wait for voices to be loaded before setting voice
  if ('speechSynthesis' in window) {
    const voices = speechSynthesis.getVoices()
    
    // Try to find a voice that matches the language
    const preferredVoice = voices.find(voice => {
      const langCode = languageCodes[language] || 'en-US'
      return voice.lang.includes(langCode.split('-')[0]) // Match primary language code
    })
    
    // If no specific voice found, try to find any voice for the region
    const fallbackVoice = !preferredVoice && voices.find(voice => {
      const langCode = languageCodes[language] || 'en-US'
      return voice.lang.includes(langCode.split('-')[1]) // Match region code
    })
    
    if (preferredVoice) {
      utterance.voice = preferredVoice
    } else if (fallbackVoice) {
      utterance.voice = fallbackVoice
    }
  }
  
  return utterance
}
